package Tokens;

class LiteralNumbers
{
	public static void main(String[] args) 
	{
		System.out.println(1);//integer
		System.out.println(-19);//integer
		System.out.println(+1);//integer
		
		System.out.println(20.0);//double/float
		System.out.println(+10.2);//double
		System.out.println(-1.2);//double


	}
}
